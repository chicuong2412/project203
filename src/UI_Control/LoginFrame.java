    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String usernameText = usernameTextField.getText();
        String passwordText = String.valueOf(passwordTextField.getPassword());
        if (checkAccountExisted(usernameText)){
            Account acc = getAccount(usernameText);
            
            if (acc.getPassword().equals(passwordText)){
                new MainMenu(acc).setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Wrong password", 
                        "Error",
                        JOptionPane.ERROR_MESSAGE,
                        new ImageIcon(GetImage.class.getResource("cancel.png")));
            }
        } else {
            JOptionPane.showMessageDialog(this, "Wrong username", 
                    "Error",
                        JOptionPane.ERROR_MESSAGE,
                        new ImageIcon(GetImage.class.getResource("cancel.png")));
        }
    }//GEN-LAST:event_loginButtonActionPerformed

