    private void showInfo() {
        roundIDTextField.setText(course.getID());
        roundNameTextField.setText(course.getName());
        roundPrerequisitesTextField.setText(getPrerequisitesString(course));
        roundStatusTextField.setText((course.isStatus()) ? "Completed" : "Not completed");
        roundGPATextField.setText((course.isStatus()) ? String.valueOf(course.getGPA()) : "None");
        roundCreditTextField.setText(String.valueOf(course.getCredit()));
        String reference = course.getReference();
        StringBuilder output = new StringBuilder();
        String [] toTextArea = reference.split("/");
        for (int i = 0; i < toTextArea.length; i++){
            output.append(toTextArea[i]).append("\n");
        }
        referenceTextArea.setText(output.toString());
    }
